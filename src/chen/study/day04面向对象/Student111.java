package chen.study.day04面向对象;

public class Student111 {
    private String name;
    private int age;
    private boolean male;

    public void setName(String str) {
        name = str;
    }

    public String getName() {
        return name;
    }

    public void setAge(int num) {
        age = num;
    }

    public int getAge(){
        return age;
    }

    public void setMale(boolean a){
        male = a;
    }

    public boolean isMale(){
        return male;
    }
}
